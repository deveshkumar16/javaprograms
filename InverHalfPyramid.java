class InverHalfPyramid{
    
        public static void main(String[] args){
            int i,j;
            int n=4;
            for(i=1;i<=n;i++)
            {
                for(j=i;j<=n;j++){
                    System.out.print("*");
                }
                System.out.println();
    
        }
    }
    }
